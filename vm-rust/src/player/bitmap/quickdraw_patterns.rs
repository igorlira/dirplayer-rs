/// Returns the 8x8 1-bit pattern for a shape `pattern` value (1..=56), else None.
pub fn quickdraw_pattern(pattern: u16) -> Option<&'static [u8; 8]> {
    if pattern >= 1 && pattern <= 56 {
        QUICKDRAW_PATTERNS.get((pattern - 1) as usize)
    } else {
        None
    }
}
